	 . 설정 시 테이블의 데이터를 전부 정렬하기 때문에 
	   많은 양의 데이터가 존재할 경우 시스템 부하가 발생. 
	 . 비클러스터 형 보다 검색 속도가 빠르나 입력/수정/삭제 느림.

   - 비 클러스터 형.(찾아보기) 
     . 테이블 당 여러개 생성 가능
	 . 테이블의 데이터는 그냥 둔 상태로 별도의 인덱스 페이지를 생성하여 관리
	 . 클러스터 형 보다 검색은 느리지만 입력/수정/삭제 가 빠름. 
	 . 너무 많이 설정하면 오히려 성능을 저하. 

